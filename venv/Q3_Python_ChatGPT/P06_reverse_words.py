# Write a function called reverse_words that takes a string as input and returns the string with each word reversed.
# For example, if the input string is "Hello, world!", the function should return "olleH, !dlrow".

def reverse_words(text):
    return print(text[::-1])

text = input("Enter your text here : ")
reverse_words(text)



